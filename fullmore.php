                     <p class="custom-style heading" >2. Chhole (Chickpea Curry)


                    </p>
                <img src="img/4.jpg" style="width:650px;height:400px;" class="img align-with-text" alt="" />
                <br>
                <p class="custom-paragraph">
                    Calories – 409; Serves – 1; Cooking Time – 15 mins </p> 
                    <br>
                    <p class="custom-paragraph">
                        This healthy Indian recipe is a flavorful chickpea curry that you can make in just 20 minutes. Also called chana masala, this dish is a comforting and delicious dinner.</p>
                        <br>
                    <p class="custom-style heading" >Ingredients   </p>
                    <ul class="custom-list">            
                    <li>1 medium serrano pepper, cut into thirds                    </li>
                    <li>4 large cloves garlic                    </li>
                    <li> 1 2-inch piece fresh ginger, peeled and coarsely chopped                    </li>
                    <li> 1 medium yellow onion, chopped (1-inch)                    </li>
                    <li>2 teaspoons ground coriander                    </li>
                    <li>Salt to taste                    </li>
                    <li>½ teaspoon ground turmeric                    </li>
                    <li>2 ¼ cups no-salt-added canned diced tomatoes with their juice (from a 28-ounce can)                    </li>
                    <li>2 15-ounce cans chickpeas, rinsed                    </li>
                    <li>2 teaspoons garam masala                    </li>
                    <li>Fresh cilantro for garnish                    </li>

                  </ul> 
                   <br>
                   <p class="custom-style heading" > Directions </p>
                   <br>
                   <p class="subheading">Step 1</p> 
                   <p class="custom-paragraph">
                    Pulse serrano, garlic and ginger in a food processor until minced. Scrape down the sides and pulse again. Add onion; pulse until finely chopped, but not watery.</p>
                    
                    <p class="subheading">Step 2</p> 
                    <p class="custom-paragraph">
                        Heat oil in a large saucepan over medium-high heat. Add the onion mixture and cook, stirring occasionally, until softened, 3 to 5 minutes. Add coriander, cumin and turmeric and cook, stirring, for 2 minutes.

                    </p>
                     
                     <p class="subheading">Step 3</p> 
                    <p class="custom-paragraph">
                        Pulse tomatoes in the food processor until finely chopped. Add to the pan along with salt. Reduce heat to maintain a simmer and cook, stirring occasionally, for 4 minutes. Add chickpeas and garam masala, reduce heat to a gentle simmer, cover and cook, stirring occasionally, for 5 minutes more. Serve topped with cilantro, if desired</p>
                     
                     
