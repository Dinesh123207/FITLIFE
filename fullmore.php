                <p class="custom-style heading" >1. Herbed Chicken Marsala                </p>
                <img src="img/3.jpg" style="width:650px;height:400px;" class="img align-with-text" alt="" />
                <br>
                <p class="custom-paragraph">
                    Eating chicken herbed masala not only tantalizes the taste buds but also provides a plethora of nutrition benefits that align well with fitness goals. Chicken is an excellent source of lean protein, essential for muscle repair and growth. 
                    <br>The combination of herbs in the masala introduces a spectrum of antioxidants and anti-inflammatory compounds, contributing to overall well-being. Herbs such as coriander, cumin, and turmeric not only enhance the flavor but also bring forth potential health benefits, including improved digestion and anti-inflammatory effects.</p> 
                    <br>
                     
                    <p class="custom-style heading" >Ingredients   </p>
                    <ul class="custom-list">            
                    <li>Four 4-ounce boneless, skinless chicken breast cutlets </li>
                    <li>Kosher salt and freshly ground black pepper                    </li>
                    <li> 1/3 cup whole wheat flour                    </li>
                    <li> 1 1/2 tablespoons extra-virgin olive oil                    </li>
                    <li>3/4 cup low-sodium chicken broth                    </li>
                    <li>1/2 teaspoon finely chopped rosemary                    </li>
                    <li> 10 ounces white button or cremini (baby bella) mushrooms, sliced                    </li>
                    <li> 2 teaspoons unsalted butter                    </li>
                    <li> 1 to 2 tablespoons roughly chopped flat-leaf parsley                    </li>
                    </ul> 
                   <br>
                   <p class="custom-style heading" > Directions </p>
                   <br>
                   <p class="subheading">Step 1</p> 
                   <p class="custom-paragraph">
                    Place the chicken cutlets between 2 pieces of plastic wrap and pound with a meat mallet (or the flat side of a chef's knife) until about 1/3-inch thick. Sprinkle with 1/4 teaspoon salt and 1/4 teaspoon pepper.</p>
                    
                    <p class="subheading">Step 2</p> 
                    <p class="custom-paragraph">
                        Put the flour on a medium plate. Heat the oil in a large nonstick skillet over medium-high heat. Dredge the chicken in the flour to fully coat, shaking off any excess.</p>
                     
                     <p class="subheading">Step 3</p> 
                    <p class="custom-paragraph">
                        Add 1/2 cup of the broth, the sun-dried tomatoes and rosemary to any remaining drippings in the skillet and cook, stirring frequently, for 1 minute to plump the tomatoes. Add the mushrooms, 1/4 teaspoon salt and 1/2 teaspoon pepper and cook until the mushrooms are soft, about 5 minutes.</p>
                     
                     <p class="subheading">Step 4</p> 
                    <p class="custom-paragraph">
                        Spoon the mushroom mixture and sauce over the chicken, sprinkle with the parsley and serve.</p>
